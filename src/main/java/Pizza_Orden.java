public class Pizza_Orden {
    private int noP = 0;
    private String nombre_ordenP;
    private String descripcion_ordenP;
    private String sucursal_P;
    
    public Pizza_Orden(){
        
    }

    public String getNombre_ordenP() {
        return nombre_ordenP;
    }

    public void setNombre_ordenP(String nombre_ordenP) {
        this.nombre_ordenP = nombre_ordenP;
    }

    public String getDescripcion_ordenP() {
        return descripcion_ordenP;
    }

    public void setDescripcion_ordenP(String descripcion_ordenP) {
        this.descripcion_ordenP = descripcion_ordenP;
    }

    public String getSucursal_P() {
        return sucursal_P;
    }

    public void setSucursal_P(String sucursal_P) {
        this.sucursal_P = sucursal_P;
    }

    public int getNoP() {
        return noP;
    }

    public void setNoP(int noP) {
        this.noP = noP;
    }
  
    
}
